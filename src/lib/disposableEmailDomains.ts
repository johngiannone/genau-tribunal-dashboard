/**
 * Check if an email domain is in the disposable list
 */
export function isDisposableEmail(email: string): boolean {
  const domain = email.toLowerCase().split('@')[1];
  if (!domain) return false;
  
  return DISPOSABLE_EMAIL_DOMAINS.includes(domain);
}

/**
 * Get a friendly error message for disposable email
 */
export function getDisposableEmailError(): string {
  return 'Temporary or disposable email addresses are not allowed. Please use a permanent email address.';
}
